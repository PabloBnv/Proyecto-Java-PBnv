
public class libro {

    public String nombre;
    public String ISBN;
    public String titulo;
    public int numeroPag;

    public libro() {

    }

    public libro(String nombre, String iSBN, String titulo, int numeroPag) {
        this.nombre = nombre;
        this.ISBN = iSBN;
        this.titulo = titulo;
        this.numeroPag = numeroPag;
    }
    


}
